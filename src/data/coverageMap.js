export function getCoverageSummary(items = coverageMap) {
  const summary = {
    total: items.length,
    covered: items.filter((item) => item.status === 'covered').length,
    partial: items.filter((item) => item.status === 'partial').length,
    missing: items.filter((item) => item.status === 'missing').length,
    highTotal: items.filter((item) => item.importance === 'high').length,
    highCovered: items.filter((item) => item.importance === 'high' && item.status === 'covered').length,
    byModule: {}
  };
  summary.coveragePercent = summary.total ? Math.round((summary.covered / summary.total) * 100) : 0;
  summary.highCoveragePercent = summary.highTotal ? Math.round((summary.highCovered / summary.highTotal) * 100) : 0;
  items.forEach((item) => {
    summary.byModule[item.module] ||= { total: 0, covered: 0, partial: 0, missing: 0 };
    summary.byModule[item.module].total += 1;
    summary.byModule[item.module][item.status] += 1;
  });
  return summary;
}

export function getCoverageByTopic(topic) {
  return coverageMap.find((item) => item.topic === topic) || coverageMap.find((item) => topic?.startsWith(`${item.module}:`));
}

const prerequisiteLabels = {
  'matrix-vector-basics': '向量和矩阵基础',
  'matrix-multiplication': '矩阵乘法',
  'matrix-determinants': '行列式基础',
  'matrix-powers': '矩阵乘方'
};

function prerequisiteTitle(id) {
  return coverageMap.find((item) => item.id === id)?.title || prerequisiteLabels[id] || id;
}

export function getBeginnerSupport(topic, fallbackModule = 'matrix') {
  const item = getCoverageByTopic(topic) || coverageMap.find((entry) => entry.module === fallbackModule);
  return {
    prerequisites: item?.prerequisites?.length ? item.prerequisites.map(prerequisiteTitle).join('、') : '基础符号和本节定义',
    explanation: item?.beginnerNote || '先回到本节的生活化解释，再做一道引导题。',
    lessonRoute: item?.lessonRoute || `#/lesson?module=${fallbackModule}`,
    questionRoute: item?.guidedQuestionIds?.[0]
      ? `#/practice?module=${item.module}&question=${item.guidedQuestionIds[0]}`
      : `#/practice?module=${fallbackModule}`,
    examNote: item?.examNote || '先完成基础题，再进入考试题。'
  };
}
